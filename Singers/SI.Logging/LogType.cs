namespace RSS
{
    /// <summary>
    /// Enumeration of log types.
    /// </summary>
    public enum LogType
    {
        /// <summary>
        /// Debug information.
        /// </summary>
        Debug,
        /// <summary>
        /// Information.
        /// </summary>
        Info,
        /// <summary>
        /// A warning.
        /// </summary>
        Warning,
        /// <summary>
        /// A non-fatal error.
        /// </summary>
        Error,
        /// <summary>
        /// A fatal error.
        /// </summary>
        Fatal
    }
}
